//Lớp và phương thức trừu tượng abstract trong Java
public class ToaDo {
    int x;
    int y;

    public ToaDo(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }
}