//Các phép toán một ngôi trong Java
public class ViDu {
    public static void main(String[] args) {
        int a = 7;
        System.out.println("+ => Gia tri duong a la: "+(+a));
        System.out.println("- => Gia tri am a la: "+(-a));
        boolean b = false;
        System.out.println("!b => Phu dinh cua b la: "+(!b));

        System.out.println("a = "+a);
        System.out.println("++a = "+ (++a));
        System.out.println("a++ = "+ (a++));

        System.out.println("a = "+a);
        System.out.println("--a = "+(--a));
        System.out.println("a-- = "+(a--));

        System.out.println("a = "+a);
    }
}
