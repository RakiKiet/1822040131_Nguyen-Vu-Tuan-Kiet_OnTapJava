//Cách sử dụng vòng lặp for trong Java
public class ViDu {
    public static void main(String[] args) {
        for(int i = 1; i <= 100; i++) {
            System.out.println("Bai tap thu " + i + " trong ngay.");
        }
        for(int i = 100; i > 0; i--) {
            System.out.println("So lan lam bai tap: " + i);
        }
    }
}